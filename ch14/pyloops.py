# while loop
count = 1
while count <= 10:
    print("The count is: " + str(count))
    count += 1

# for Loop
colors = ["blue","green","red","purple"]
for color in colors:
    print("the current color is: " + color)

# for loop using dict
ages = {'Richard': 50, 'Nina': 40, 'Mathew': 20}
for key, value in ages.items():
    print("The key is: " + key)
    print("The value is: " + str(value))

